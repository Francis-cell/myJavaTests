public class ArrayUtils {

    /**
     * 将数组arr中的元素全部打印出来
     * @param arr
     */
    public static void printArray(int arr[]) {
        for (int i : arr) {
            System.out.print(i + " ");
        }
        System.out.println();
    }
}
